CREATE TABLE WorkItemResult
(
    [Id] BIGINT PRIMARY KEY IDENTITY(1, 1) NOT NULL,
    [WorkItemId] BIGINT NOT NULL,
    [StatusId] SMALLINT NOT NULL,
    [MachineId] SMALLINT NOT NULL,
    [FlowId] SMALLINT NOT NULL,
    [Reason] NVARCHAR(255) NULL,
    [Message] NVARCHAR(MAX) NOT NULL,
    [ProcessingStartUTC] DATETIMEOFFSET(3) NOT NULL,
    [ProcessingEndUTC] DATETIMEOFFSET(3) NOT NULL,
    [InsertedAtUTC] DATETIMEOFFSET(3) NOT NULL DEFAULT TODATETIMEOFFSET(GETUTCDATE(), 0)
)